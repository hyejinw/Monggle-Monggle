export const DIRECT_CRISIS_PATTERNS = [
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}죽고\s*싶/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}살고\s*싶지\s*않/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}삶을\s*끝내/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}자살/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}자해/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}사라지고\s*싶/,
  /(나|내가|나는|난|저|제가|전)\s*.{0,12}목숨을\s*끊/,
  /오늘\s*(죽|끝내|자살|자해)/,
  /당장\s*(죽|자살|자해)/,
];

