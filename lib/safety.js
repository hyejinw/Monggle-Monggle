  "지금은 혼자 있지 말고, 바로 연락할 수 있는 가까운 사람이나 지역의 응급기관·전문 위기지원에 연결해줘.",
  "당장 자신을 다치게 할 가능성이 있다면 이 앱의 대화보다 긴급한 도움을 먼저 받아야 해.",
